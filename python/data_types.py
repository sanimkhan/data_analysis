# Arrays
import array as arr
my_array = arr.array('i', [1, 2, 3, 4])
my_array.append(5)
print(my_array)  # Output: array('i', [1, 2, 3, 4, 5])

# Lists
my_list = [1, "two", 3, "four"]
my_list.append("five")
print(my_list)  # Output: [1, "two", 3, "four", "five"]

# Tuples
my_tuple = (1, "two", 3, "four")
print(my_tuple)  # Output: (1, "two", 3, "four")


# Sets
my_set = {1, "two", 3, "four"}
my_set.add("five")
print(my_set)  # Output: {1, 3, "two", "four", "five"}

# Dictionaries
my_dict = {"one": 1, "two": 2, "three": 3}
my_dict["four"] = 4
print(my_dict)  # Output: {"one": 1, "two": 2, "three": 3, "four": 4



